import java.util.*;
class numpat
{
    public static void main(String args[])
    {
       Scanner obj = new Scanner(System.in);
       int m = obj.nextInt();
       int r= 1;
       for(int i=1;i<m;i++,System.out.println())
       {
        for(int j=0;j<i;j++)
        {
            System.out.print(r + " " );
            r++;
        }
       }
    }
}
      
